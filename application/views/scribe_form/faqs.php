			function refresh_captcha_img()
			{
				$(".loading").show();
				$.ajax(
				{
					type: 'POST',
					url: '<?php echo site_url("Scribe_form/generate_captcha_ajax/"); ?>',
					data: { "session_name":"LOGIN_SCRIBE" },
